import SwiftUI


extension Color {

    public static let element = Color(.element)
    public static let highlight = Color(.highlight)
    public static let shadow = Color(.shadow)
    
    public static let selectChip = Color(.selectChip)
    public static let unselectChip = Color(.unselectChip)
    
    public static let greenColor = Color("Push", bundle: .module)
    
    public static let systemWhite = Color(.systemWhite)
    public static let systemBlack = Color(.systemBlack)
    public static let systemRed = Color(.systemRed)

}

